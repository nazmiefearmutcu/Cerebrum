def run_flat_memorizer(task):
    """Pure-memorizer control: stores each SEEN whole observation; at test, completes the
    f2-part by nearest-neighbour over stored f1-parts (recall the f2 of the most similar SEEN
    f1-part). It has NO notion of factors, so on held-out combos it returns the f2 of whatever
    seen combo shared the same (or closest) f1-part — which is by construction the WRONG f2 for
    a genuinely compositional holdout. This is the 'cannot compose' floor."""
    store_f1 = np.array([task.f1_part(f1) for (f1, f2) in task.train_combos])
    store_f2lbl = [f2 for (f1, f2) in task.train_combos]
    held = task.heldout_combos
    if not held:
        return float("nan")
    ok = 0
    for (f1, f2) in held:
        q = task.f1_part(f1)
        d = np.sum((store_f1 - q[None, :]) ** 2, axis=1)
        # break exact-f1-part ties deterministically by first occurrence (argmin does this)
        pred_f2 = store_f2lbl[int(np.argmin(d))]
        if pred_f2 == (f2 % task.B):
            ok += 1
    return ok / len(held)


def run_backprop_mlp(task, epochs=400, hidden=32, lr=0.2, seed=0):
    """Backprop-MLP COMPARATOR (uses backprop, which CEREBRUM never does). Maps the f1-part ->
    f2-part by gradient descent on the TRAIN combos, tested on held-out combos via nearest-f2.

    With a factorable target a backprop net CAN learn the f1->f2 association for seen rows, but
    for a held-out (f1,f2) the correct f2 was never paired with this f1 in training, so the MLP
    regresses toward the f2 it DID see for this f1 (or an average) — it generalizes
    compositionally only to the extent the target function is smooth across factors, which for
    independent random parts it is not. Reported as a reference, not a target."""
    rng = np.random.default_rng(seed)
    nin = task.f1_dim
    nout = task.f2_dim
    W1 = 0.1 * rng.standard_normal((hidden, nin)); b1 = np.zeros(hidden)
    W2 = 0.1 * rng.standard_normal((nout, hidden)); b2 = np.zeros(nout)

    X = np.array([task.f1_part(f1) for (f1, f2) in task.train_combos])
    Y = np.array([task.f2_part(f2) for (f1, f2) in task.train_combos])
    if X.shape[0] == 0:
        return float("nan")
    n = X.shape[0]
    for _ in range(epochs):
        h = np.tanh(X @ W1.T + b1)              # (n,hidden)
        yhat = h @ W2.T + b2                    # (n,nout) linear output
        gy = (yhat - Y) / n                     # dL/dyhat (MSE)
        gW2 = gy.T @ h; gb2 = gy.sum(0)
        gh = (gy @ W2) * (1 - h ** 2)
        gW1 = gh.T @ X; gb1 = gh.sum(0)
        W2 -= lr * gW2; b2 -= lr * gb2; W1 -= lr * gW1; b1 -= lr * gb1

    held = task.heldout_combos
    if not held:
        return float("nan")
    ok = 0
    for (f1, f2) in held:
        h = np.tanh(task.f1_part(f1) @ W1.T + b1)
        yhat = h @ W2.T + b2
        if task.nearest_f2(yhat) == (f2 % task.B):
            ok += 1
    return ok / len(held)
