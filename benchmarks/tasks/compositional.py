# CEREBRUM PC-hierarchy completion runner (NO grid head: the deep PC areas do the work)
# ------------------------------------------------------------------------------------------

def _bw_alignment(net):
    """Mean cosine similarity between B[l].T and W[l] across layers (KP alignment metric).
    1.0 == perfect feedback alignment (B == W.T up to scale). This only READS B and W to
    MEASURE alignment for reporting; it is NOT used inside any learning rule (no transport)."""
    cos = []
    for l in range(net.L - 1):
        a = net.B[l].T.ravel()
        b = net.W[l].ravel()
        na = np.linalg.norm(a); nb = np.linalg.norm(b)
        if na > 1e-12 and nb > 1e-12:
            cos.append(float(a @ b / (na * nb)))
    return float(np.mean(cos)) if cos else float("nan")


def _train_pc(task, cfg, passes, eta_w_scale=0.6, tau_w=1.0, tau_e=1.0, align_curve=None):
    """Online local-plasticity training of a bare PCAreas hierarchy on the TRAIN combos.

    Mirrors the continual.py reconstruction loop: settle (noisy) with the full observation
    clamped at the bottom, low-pass the presynaptic latent into an eligibility trace, then a
    four-factor local weight update gated by a scalar M (constant reward=1 -> M>0). No grid
    head, no backprop, no W.T."""
    net = PCAreas(cfg)
    nm = Neuromodulator(cfg)
    rng = SeededRNG(cfg.seed)
    elig = [Eligibility((cfg.dims[l + 1],), cfg) for l in range(net.L - 1)]
    eta = eta_w_scale / tau_w

    combos = list(task.train_combos)
    order_rng = np.random.default_rng(cfg.seed + 99)
    for p in range(passes):
        if align_curve is not None:
            align_curve.append(_bw_alignment(net))   # record B<->W.T cosine per pass
        order = combos[:]
        order_rng.shuffle(order)
        for (f1, f2) in order:
            obs = task.embed(f1, f2)
            for _ in range(cfg.n_settle):
                net.settle_step(rng, T=cfg.T_floor, clamp_bottom=obs)
                for l in range(net.L - 1):
                    elig[l].step(a_pre=net.x[l + 1])
            net.compute_errors()
            M = nm.update(reward=1.0)
            for l in range(net.L - 1):
                dW = weight_update(
                    M=M, theta=np.ones_like(net.W[l]),
                    Pi_post=net.Pi[l], eps_post=net.eps[l],
                    elig=elig[l].value, eta=eta,
                )
                if cfg.align_feedback:
                    # Kolen-Pollack: matched decay on W + transposed same product on B.
                    net.W[l] += dW - cfg.lam_kp * net.W[l]
                    net.B[l] += feedback_update_kp(
                        net.B[l], M=M, Pi_post=net.Pi[l], eps_post=net.eps[l],
                        elig=elig[l].value, eta=eta, lam_kp=cfg.lam_kp,
                    )
                else:
                    net.W[l] += dW
                    net.B[l] += (1.0 / cfg.tau_b) * feedback_update(
                        net.B[l], a_up=net.x[l + 1], eps=net.eps[l], cfg=cfg
                    )
                net.Pi[l] = precision_update(net.Pi[l], eps_sq=net.eps[l] ** 2, cfg=cfg)
    return net


def _complete_f2(net, task, f1, settle_steps):
    """Masked PC pattern-completion: clamp ONLY the f1-part of x[0], leave the f2-part FREE,
    settle NOISE-FREE (T=0), return the completed f2-part.

    Partial clamp is done benchmark-side (no cerebrum/ change): settle_step with clamp_bottom=None
    lets all of x[0] evolve, then we re-pin ONLY the observed f1-part dims after every step;
    the masked f2-part dims keep their settled (top-down-predicted) values."""
    erng = SeededRNG(_EVAL_SEED)  # deterministic eval rng (T=0 anyway -> zero noise)
    f1p = task.f1_part(f1)
    # initialize x[0]: observed part = clamp, masked part = 0 (free start), latents = 0
    net.x = [np.zeros_like(xl) for xl in net.x]
    net.x[0][task.f1_slice] = f1p
    for _ in range(settle_steps):
        net.settle_step(erng, T=0.0, clamp_bottom=None)
        net.x[0][task.f1_slice] = f1p  # re-pin observed dims; masked dims evolve freely
    return np.asarray(net.x[0][task.f2_slice])


def run_pc_completion(task, cfg, passes=60, settle_steps=None):
    """Train a bare PC hierarchy, then score masked f2-completion on held-out + train combos.

    Returns acc_heldout (compositional generalization) and acc_train (the model can at least
    complete combos it was trained on — a sanity reference), plus depth = #PC areas."""
    if settle_steps is None:
        settle_steps = max(cfg.n_settle, 20)
    align_curve = []
    net = _train_pc(task, cfg, passes, align_curve=align_curve)
    align_curve.append(_bw_alignment(net))   # final alignment after the last pass

    # mechanistic diagnostics so a null is self-explained in the output:
    #  - lat_act: mean |x[l]| of the LATENT areas (l>=1) when a TRAIN obs is clamped & settled.
    #    A near-silent latent (lat_act ~ 0) means the hierarchy never built a code that could be
    #    read back during completion -> depth cannot be consulted.
    #  - comp_norm: mean L2 norm of the completed (free) f2-part across train combos. If this is
    #    ~0 the completion is just picking the nearest prototype to a near-zero vector (geometry,
    #    not factorization), which is why accuracy is depth-insensitive.
    erng = SeededRNG(_EVAL_SEED + 1)
    lat_vals, comp_norms = [], []
    for (f1, f2) in task.train_combos:
        obs = task.embed(f1, f2)
        for _ in range(settle_steps):
            net.settle_step(erng, T=0.0, clamp_bottom=obs)
        lat_vals.append(np.mean([np.abs(np.asarray(net.x[l])).mean() for l in range(1, net.L)]))
        comp_norms.append(float(np.linalg.norm(_complete_f2(net, task, f1, settle_steps))))

    def _score(combos):
        if not combos:
            return float("nan")
        ok = 0
        for (f1, f2) in combos:
            comp = _complete_f2(net, task, f1, settle_steps)
            if task.nearest_f2(comp) == (f2 % task.B):
                ok += 1
        return ok / len(combos)

    return {
        "acc_heldout": _score(task.heldout_combos),
        "acc_train": _score(task.train_combos),
        "depth": net.L,
        "n_train": len(task.train_combos),
        "n_heldout": len(task.heldout_combos),
        "lat_act": float(np.mean(lat_vals)) if lat_vals else float("nan"),
        "comp_norm": float(np.mean(comp_norms)) if comp_norms else float("nan"),
        "align_start": align_curve[0] if align_curve else float("nan"),
        "align_end": align_curve[-1] if align_curve else float("nan"),
        "align_curve": align_curve,
    }


