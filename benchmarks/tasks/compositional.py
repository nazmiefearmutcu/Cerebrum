"""COMPOSITIONAL-GENERALIZATION probe that consults the DEEP PC HIERARCHY (not the grid HEAD).

WHY THIS EXISTS
---------------
The earlier I6 "depth doesn't help" result was a NULL for a boring reason: Task-1 completion
reads the grid-HEAD content store, so extra PC error-neuron areas are never consulted — adding
PC depth literally could not change that readout. This probe removes the grid HEAD entirely and
makes the PC HIERARCHY itself do the completion, so depth (number of PC areas) is now on the
causal path. We then HONESTLY report whether 2 vs 3 vs 4 PC areas changes compositional
generalization.

THE TASK
--------
Inputs are generated by two INDEPENDENT latent factors f1 in {0..A-1}, f2 in {0..B-1}. The
observation is a FIXED COMPOSITIONAL embedding:

    obs(f1,f2) = concat( P1[f1] , P2[f2] )            # f1-part then f2-part

where P1[f1] (resp. P2[f2]) is a frozen random vector that depends ONLY on f1 (resp. f2).
Because the f1-part is a pure function of f1 and the f2-part a pure function of f2, the input
is genuinely FACTORABLE: a model that discovers the two factors can predict the f2-part of an
unseen (f1,f2) combo from the f1-part it shares with a SEEN combo. A pure memorizer that binds
whole observations cannot.

TRAIN / TEST SPLIT
------------------
We train (online, local four-factor plasticity, Langevin noise T=T_floor) on a SUBSET of the
A*B combos and HOLD OUT the rest, such that every factor VALUE in a held-out combo was still
seen in training within some OTHER combo (so completion is possible in principle — a fair probe,
not a trivially-unsolvable one).

COMPLETION READOUT (the probe)
------------------------------
For a held-out combo (f1,f2): CLAMP only the f1-part dims of x[0] to P1[f1], leave the f2-part
dims FREE, settle the hierarchy NOISE-FREE (T=0), and read the completed f2-part. Score = does
the completed f2-part match the TRUE held-out f2 better than any other f2 candidate (1-NN over
the B prototype f2-parts). This is masked PC pattern-completion through the hierarchy.

IMPLEMENTATION NOTE (no cerebrum/ change)
--------------------------------------
Masked / partial completion is done WITHOUT modifying cerebrum/: we call settle_step with
clamp_bottom=None (so all of x[0] is free to evolve under the PC drift), then immediately
re-pin ONLY the observed (f1-part) dims of net.x[0] after each step. The masked (f2-part) dims
keep their settled values. This is a pure benchmark-side use of the existing settle_step — no
new clamp_mask argument and no edit to cerebrum/ was needed.

HONESTY
-------
Nothing here is engineered to read the answer from the bottom area. The f2-part is genuinely
FREE during completion; it can only be filled by top-down prediction from the latent areas,
i.e. by whatever factorization the local plasticity actually built. If deeper hierarchies do
not generalize better, that is a valid negative result and the run script reports it as such.
"""
import numpy as np

from cerebrum.pc_core import PCAreas
from cerebrum.plasticity import (
    Eligibility, weight_update, precision_update, feedback_update, feedback_update_kp,
)
from cerebrum.neuromod import Neuromodulator
from cerebrum.rng import SeededRNG

# Scale the embedding into the tanh decoder's representable range (same rationale as the
# continual harness PROTO_SCALE): keeps the reconstruction floor off saturation so the
# hierarchy can actually fit the parts.
PART_SCALE = 0.6

# Fixed seed for the NOISE-FREE measurement readout (same convention as continual._EVAL_SEED):
# the completion is a pure function of the learned weights, not of settling noise.
_EVAL_SEED = 0xC0FFEE


class CompositionalTask:
    """Two-factor compositional embedding + train/holdout split over (f1,f2) combos."""

    def __init__(self, A=4, B=4, part_dim=8, n_heldout=None, seed=0):
        self.A = int(A)
        self.B = int(B)
        self.f1_dim = int(part_dim)
        self.f2_dim = int(part_dim)
        self.obs_dim = self.f1_dim + self.f2_dim
        self.f1_slice = slice(0, self.f1_dim)
        self.f2_slice = slice(self.f1_dim, self.obs_dim)

        rng = np.random.default_rng(seed)
        # frozen per-value parts: P1[f1] depends only on f1, P2[f2] only on f2 (factorable)
        self.P1 = PART_SCALE * rng.standard_normal((self.A, self.f1_dim))
        self.P2 = PART_SCALE * rng.standard_normal((self.B, self.f2_dim))

        # ---- train / holdout split over the A*B grid of combos ----------------------------
        # Hold out a diagonal-ish set of combos but NEVER a combo whose factor value is unseen
        # elsewhere in training. We hold out (i, (i+1) % B) for i in 1..min(A,B)-1: this skips
        # row 0 and column 0 entirely, guaranteeing f1=i (i>=1) appears in training via other
        # columns and f2=(i+1)%B appears via other rows. Result: a clean compositional holdout.
        all_combos = [(f1, f2) for f1 in range(self.A) for f2 in range(self.B)]
        held = []
        m = min(self.A, self.B)
        for i in range(1, m):
            f2 = (i + 1) % self.B
            held.append((i, f2))
        if n_heldout is not None:
            held = held[: int(n_heldout)]
        held_set = set(held)
        train = [c for c in all_combos if c not in held_set]

        # safety: drop any held combo whose factor value is not seen in training elsewhere
        seen_f1 = {f1 for (f1, _) in train}
        seen_f2 = {f2 for (_, f2) in train}
        held = [(f1, f2) for (f1, f2) in held if f1 in seen_f1 and f2 in seen_f2]

        self.train_combos = train
        self.heldout_combos = held

    def embed(self, f1, f2):
        v = np.empty(self.obs_dim)
        v[self.f1_slice] = self.P1[f1 % self.A]
        v[self.f2_slice] = self.P2[f2 % self.B]
        return v

    def f1_part(self, f1):
        return self.P1[f1 % self.A].copy()

    def f2_part(self, f2):
        return self.P2[f2 % self.B].copy()

    def nearest_f2(self, vec_f2):
        """1-NN classify a completed f2-part vector against the B frozen f2 prototypes."""
        d = np.sum((self.P2 - vec_f2[None, :]) ** 2, axis=1)
        return int(np.argmin(d))


